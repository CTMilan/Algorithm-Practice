package ctmilan.practice;

public class Node {

    private int id;
    private int weight;

    public Node(int id, int weight) {
        this.weight = weight;
        this.id = id;
    }

    public int getWeight() {
        return weight;
    }
    public void setWeight(int weight) {
        this.weight = weight;
    }
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    

}
